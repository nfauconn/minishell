#include "reset.h"

void	reset_sh(t_sh *sh)
{
	if (sh->cmd_list)
		clear_cmd_list(sh->cmd_list);
	sh->cmd_list = NULL;
	sh->cmd_nb = 0;
	sh->heredoc_nb = 0;
}
