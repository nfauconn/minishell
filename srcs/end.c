#include "minishell.h"

void	end(t_input *input, t_sh *sh)
{
	if (input->line_read)
		ft_strdel(&input->line_read);
	if (input->token_list)
		ft_lstclear(&input->token_list, free);
	input = NULL;
	if (sh->env)
		ft_lstclear(&sh->env, free);
}
