#include "libft.h"

void	ft_lstdisplay(t_list *lst)
{
	int	i;

	i = 0;
	while (lst)
	{
		ft_printf("elem%d = [%s]\n", i, (char *)lst->content);
		lst = lst->next;
		i++;
	}
}